        rawDict.putAll(
            ObjectMapper().convertValue(jsonNode, object : TypeReference<Map<String, String>>() {})
        )
        val regExp = "^[\u4E00-\u9FFF]+$".toRegex()
        for (pair in rawDict) {
            if (regExp.matches(pair.key)) {
                pinyinDict[AbstractWordService.getPinyin(pair.key)] = pair.value
            }
        }
        save()
    }
